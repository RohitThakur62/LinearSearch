package Array_java;

import java.util.Arrays; 
  
// Main class  
class ArrayFunction{ 
    
    // Main driver method  
    public static void main(String[] args) 
    { 
        // Get the Array 
        int intArr[] = { 10, 20, 15, 22, 35 }; 
  
        // To convert the elements as List 
        System.out.println("Integer Array as List: "
                           + Arrays.asList(intArr)); 
    } 
}